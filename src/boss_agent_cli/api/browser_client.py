class BrowserChannelUnavailable(RuntimeError):
	"""No safe live browser channel is available for real platform requests."""

	def __init__(self) -> None:
		super().__init__(
			"BOSS 浏览器通道不可用：无法通过 CDP 复用真实 Chrome。"
			"为保护账号，live BOSS 请求已禁止降级到 Bridge 或 headless patchright。"
			"请用 --remote-debugging-port=9229 打开真实 Chrome 后重试。"
		)

class BossAccessLimitedError(RuntimeError):
	"""Existing CDP Chrome is already on a BOSS access-limited page."""

	def __init__(self, result: dict[str, Any]) -> None:
		self.result = result
		super().__init__(str(result.get("message") or "BOSS 页面访问受限"))


