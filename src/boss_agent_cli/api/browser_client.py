def _scrape_jobs_page_via_cdp(cdp_http_url: str, page_url: str) -> dict[str, Any]:
	import json as _json

	import websockets.sync.client as _ws_client

	try:
		target_ws = _pick_zhipin_target_ws(cdp_http_url, preferred_page_url=CANDIDATE_JOBS_URL)
	except BossAccessLimitedError as exc:
		return exc.result
	expression = _build_eval_expression(
		_JOBS_PAGE_FALLBACK_SCRIPT,
		{
			"timeoutMs": _BROWSER_FETCH_TIMEOUT_MS,
			"pageSizeHint": 15,
		},
	)

	with _ws_client.connect(target_ws, max_size=8 * 1024 * 1024) as ws:
		ws.send(_json.dumps({"id": 1, "method": "Page.enable"}))
		ws.send(_json.dumps({"id": 2, "method": "Runtime.enable"}))
		ws.send(_json.dumps({
			"id": 3,
			"method": "Page.navigate",
			"params": {"url": page_url},
		}))

		load_deadline = time.time() + 15.0
		while time.time() < load_deadline:
			raw = ws.recv(timeout=max(0.1, load_deadline - time.time()))
			msg = _json.loads(raw)
			if msg.get("id") == 3:
				err = msg.get("error")
				if err:
					raise RuntimeError(f"CDP Page.navigate error: {err}")
				continue
			if msg.get("method") == "Page.loadEventFired":
				break

		ws.send(_json.dumps({
			"id": 4,
			"method": "Runtime.evaluate",
			"params": {
				"expression": expression,
				"returnByValue": True,
				"awaitPromise": True,
			},
		}))

		eval_deadline = time.time() + 30.0
		while time.time() < eval_deadline:
			raw = ws.recv(timeout=max(0.1, eval_deadline - time.time()))
			msg = _json.loads(raw)
			if msg.get("id") != 4:
				continue
			err = msg.get("error")
			if err:
				raise RuntimeError(f"CDP Runtime.evaluate error: {err}")
			result = msg.get("result", {}).get("result", {})
			if "value" in result:
				return cast("dict[str, Any]", result["value"])
			exc_details = msg.get("result", {}).get("exceptionDetails")
			if exc_details:
				raise RuntimeError(
					f"JS exception: {exc_details.get('text')} — "
					f"{exc_details.get('exception', {}).get('description', '')[:300]}"
				)
			return cast("dict[str, Any]", result)
		raise RuntimeError("CDP jobs page fallback timed out after 30s")


def _cdp_evaluate_with_chat_events_in_chat_tab(
	cdp_http_url: str, script: str, arg: Any, *, listen_ms: int
) -> dict[str, Any]:
	import base64 as _base64
	import json as _json

	import websockets.sync.client as _ws_client

	target_ws = _pick_chat_target_ws(cdp_http_url)
	expression = _build_eval_expression(script, arg)

	with _ws_client.connect(target_ws, max_size=8 * 1024 * 1024) as ws:
		ws.send(_json.dumps({"id": 1, "method": "Network.enable"}))
		ws.send(_json.dumps({
			"id": 2,
			"method": "Runtime.evaluate",
			"params": {
				"expression": expression,
				"returnByValue": True,
				"awaitPromise": True,
			},
		}))

		events: list[dict[str, Any]] = []
		eval_value: Any = None
		eval_done = False
		deadline = time.time() + 30.0
		listen_deadline: float | None = None

		while True:
			now = time.time()
			if eval_done and listen_deadline is not None and now >= listen_deadline:
				return {"value": eval_value, "events": events}
			if now >= deadline:
				raise RuntimeError("CDP Runtime.evaluate timed out after 30s")

			timeout = 0.1 if eval_done and listen_deadline is not None else max(0.1, deadline - now)
			try:
				raw = ws.recv(timeout=timeout)
			except TimeoutError:
				continue

			msg = _json.loads(raw)
			msg_id = msg.get("id")
			if msg_id == 2:
				err = msg.get("error")
				if err:
					raise RuntimeError(f"CDP Runtime.evaluate error: {err}")
				result = msg.get("result", {}).get("result", {})
				exc_details = msg.get("result", {}).get("exceptionDetails")
				if exc_details:
					raise RuntimeError(
						f"JS exception: {exc_details.get('text')} — "
						f"{exc_details.get('exception', {}).get('description', '')[:300]}"
					)
				eval_value = result.get("value", result)
				eval_done = True
				listen_deadline = time.time() + (listen_ms / 1000.0)
				continue

			method = msg.get("method")
			if method not in ("Network.webSocketFrameSent", "Network.webSocketFrameReceived"):
				continue
			frame = msg.get("params", {}).get("response", {})
			if frame.get("opcode") not in (1, 2):
				continue

			payload_data = frame.get("payloadData", "")
			try:
				bs = _base64.b64decode(payload_data, validate=False)
			except Exception:
				bs = payload_data.encode("utf-8", errors="ignore")
			if len(bs) < 30:
				continue

			events.append({
				"ts": time.time(),
				"kind": "ws_send" if method.endswith("Sent") else "ws_recv",
				"bytes": len(bs),
				"utf8_bits": _carve_utf8_bits(bs)[:10],
			})
